Image* RipplingProvider::createGL()
    {
    Animable_I* ptrAnimable = RipplingProvider::createMOO();

    return new Image(ptrAnimable);
    }

Animable_I* RipplingProvider::createMOO(void)
    {
    float dt = 1;

    int dw = 16 * 60; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    return new RipplingMOO(dw, dh, dt);
    }

